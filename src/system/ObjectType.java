package system;

public class ObjectType extends MapModel{
	String id;
	Object val;
	Object desc;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public Object getVal() {
		return val;
	}
	public void setVal(Object val) {
		this.val = val;
	}
	public Object getDesc() {
		return desc;
	}
	public void setDesc(Object desc) {
		this.desc = desc;
	}
	
}
